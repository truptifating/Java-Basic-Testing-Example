public class IncreamentDecreamentOperator {

	public static void main(String[] args) {
		int x=1;
		System.out.println("Value of x befor increament is" +x);
		++x;
		System.out.println("Value of x after increament is" +x);
		x++;
		System.out.println("Value of x after increament is" +x);
		
		int y=10;
		System.out.println("Value of y befor dcreament is" +y);
		y--;
		System.out.println("Value of y after dcreament is" +y);
		
		System.out.println("Value of x befor increament is" +x++);
		System.out.println("Value of x befor increament is" + ++x);
		
		

	}

}
